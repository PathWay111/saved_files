#include "3-calc.h"
#include <stdlib.h>
#include <stdio.h>
/**
 * op_add - add t2 numbers 
 * @a: input one
 * @b: input two
 * Return: 0
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - substract 2 numbers
 * @a: input one
 * @b: input two
 * Return: 0
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiply 2 numbers
 * @a: input one
 * @b: input two
 * Return: 0
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divide 2 numbers
 * @a: input one
 * @b: input two
 * Return: 0
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - find rememder of 2 numbers
 * @a: input one
 * @b: input two
 * Return: 0
 */
int op_mod(int a, int b)
{
	return (a % b);
}
